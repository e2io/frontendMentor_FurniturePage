/**************************************************************************Share button function*****************************************************************************/
$("#share").click(function() {
  $("#shareToolTip").removeClass("shareToolTipDefault");
  /**************************************************************************toggling function*****************************************************************************/
  var clicks = $(this).data('clicks');
  if (clicks) {
    /**************************************************************************tooltip*****************************************************************************/
    $("#shareToolTip").addClass("shareToolTipRemove");
    $("#shareToolTip").removeClass("shareToolTipAdd");
    /**************************************************************************share button*****************************************************************************/
    $("#share").addClass("shareRemove");
    $("#share").removeClass("shareAdd");

  } else {
    /**************************************************************************tooltip*****************************************************************************/
    $("#shareToolTip").addClass("shareToolTipAdd");
    $("#shareToolTip").removeClass("shareToolTipRemove");
    /**************************************************************************share button*****************************************************************************/
    $("#share").removeClass("shareRemove");
    $("#share").addClass("shareAdd");
  }
  $(this).data("clicks", !clicks);

});
